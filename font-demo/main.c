void TIM3_IRQHandler()
{
  __Set( KEY_IF_RST, 0); // Clear TIM3 interrupt flag
}


void static render()
{
  char  line[32+1];
  int   row;
  int   i;

  render_text( 0, (SCREEN_HEIGHT - nice_font.height), WHITE, 1, "Hello, proportional font World!");
  line[32] = '\0';
  for ( row = 0;  row < 4;  row += 1)
  {
    for ( i = 0;  i < 32;  i += 1)
    {
      line[ i] = 32 * row + i;
    }
    if ('\0' == line[ 0])
      line[ 0] = 0x7f;
    render_text( 0, (125 - nice_font.height * row), WHITE, 0, line);
  }
}


int main()
{
  __Set( BEEP_VOLUME, 0);
  __Clear_Screen( BLACK);

  use_font( &nice_font);

  render();

  while( true)
  {
    __WFE();
  }
}

